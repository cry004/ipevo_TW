				<li>為什麼我在咖啡廳、捷運站無法使用？</li>
				<li>S0-20話機尚不支援需以「瀏覽器登入帳號密碼」之公眾無線 Wi-Fi 網路服務，因需考量到無線訊號的強度與穩定程度的影響，若距離無線基地台過遠或使用該基地台頻寬人數過多，皆會影響其通訊品質。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為什麼無法開機、充電？</li>
				<li>若發生話機無法開機，或者直接插著充電器仍無反應時，請先以旅充方式充電。以旅充方式充電靜置3~5分鐘後，再檢查話機螢幕是否有充電畫面顯示， 因話機必需有最低電量才會顯示充電畫面。</li>
			</ol>
