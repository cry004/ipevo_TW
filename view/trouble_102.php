				<li>為何我會聽到回音？</li>
				<li>通常有兩個可能性，第一個是 Skype 通訊時的網路問題， ( 除了回音之外，可能會有雜音、被搶話等不同現象。 ) 您可以掛掉後再重撥。通常重撥可以解決。但若是您持續遇到此問題，有可能是網路極端不穩定。第二個可能性是打開了 FR-33.1 的麥克風混音。以 Windows XP 為例，由 我的電腦→程式集 → 附屬應用程式 → 娛樂 → 音量控制。</li>
				<li>打開音量控制的視窗後，在「選項」→「內容」，將「混音器裝置」選擇為「 FR-33.1 Skype話機 」，並勾選下方的「麥克風」後，按「確定」。接下來，會看到音量控制變成如下畫面：</li>
				<li style="text-align: center;"><img src="images/trouble_/fr331_fq_c14.jpg"></li>
				<li>請確認右方的「麥克風」是否勾選「靜音」，若無，請勾選靜音。</li>
			</ol>
