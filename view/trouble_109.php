				<li>如果您的話機電池顯示為滿格狀態，但通話一段時間後，立即無電力反應而關機者。請參考以下方式</li>
				<li>韌體版本1.2.0.29已解決此問題，在1.2.0.29之前的版本請更新韌體。請確認您的S0-20話機?體版本是否已升級，您可以在話機（選單）（設定）（關於skype）（軟體版本）中查詢。 </br>實際通話時間3~4小時，待機時間約30小時</br>如果狀況仍相同，我們S0-20電池的保固期間自購買日後半年，若在保固期間內故障，我們將提供您免費的維修更換服務。</li>
