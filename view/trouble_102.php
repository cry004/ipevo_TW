	<h1>常見問答</h1>
	<ul>
		<li>
			<ol>
				<li>電腦接上FR-33.1話機，Skype聲音從喇叭出來，沒從話機出來？</li>
				<li>Skype的聲音無法從FR-33.1播放出來，那麼，先開啟「IPEVO Control Center」，點選「音訊設定」標籤頁，在「Skype音訊裝置選擇」項目中，將「聲音輸出」、「聲音輸入」、「鈴聲裝置」三個項目的下拉選單，分別改 成 FR-33.1，然後按下「確定」鍵，即可完成設定。</li>
				<li style="text-align: center;"><img src="images/trouble_/fr331_fq_c06.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>電腦接上FR-33.1話機，其他軟體或遊戲的音效無法正常從喇叭發音？</li>
				<li>先進入「IPEVO Control Center」、點選「音訊設定」分頁後，按下「開啟視窗系統音效控制面版」，將「音效播放預設裝置」指定在電腦原本的音效卡裝置，其他音樂或遊戲軟體即可正常發音。</li>
				<li style="text-align: center;"><img src="images/trouble_/fr331_fq_c07.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>我的FR-33.1聲音會出現異常的雜音。</li>
				<li>在我們的使用經驗中，發現當FR-33.1接上部份的 USB HUB 會出現如此的症狀。只要直接安裝到主機板上的 USB 埠即可。您可以撥打給 echo123 或是 echo-chinese 來測試您的通話品質。 目前比較新的主機板都提供了 4 到 8 個 USB 埠；因此我們建議您將FR-33.1這類不常插拔的 USB 裝置直接安插到您的主機板上。 如果您一定要使用 USB HUB 來安裝，請您在選購 USB HUB 的時候，購買「具有變壓器接頭」、以及較知名品牌的產品。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為什麼有時話機會發生無法輸入號碼的情形？</li>
				<li>在較新版本的 Skype 中，當視窗處於「通話記錄」時，不論您由電腦上的 Skype 軟體直接輸入號碼，或是由話機上按下任何號碼，均會變成「搜尋模式」。在此模式下，僅會由您的通話記錄中找尋是否有撥過您輸入的號碼。因此在這個模式下， 您並沒有辦法撥出電話。只要再壓下 S 鍵離開「通話記錄」，切換到其它模式即可。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>使用光碟或網路下載的程式安裝FR-33.1都無法完成，每次選完安裝語言後，就無法再繼續進行下一個步驟。</li>
				<li>若您的作業系統為： Windows XP Pro ，建議您重新開機後，由「開始」 → 「執行」，輸入以下命令 %USERPROFILE%Local SettingsTemp ，這個命令 將會開啟 temp 資料夾，這個資料夾是 Windows 用來存放各式各樣的暫存檔案，建議先刪掉所有檔案再進行安裝。在進行刪除之前，請先經由： 【我的電腦】 → 【工具】 → 在【資料夾選項】 → 【檢視】 → 點選「顯示所有檔案和資料夾」。</li>
