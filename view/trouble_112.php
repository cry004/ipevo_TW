<div id="trouble_Wrap">
	<h1>常見問答</h1>
	<ul>
		<li>
			<ol>
				<li>為何我無法在電腦軟體或是視訊會議程式中看到P2V的影像畫面？</li>
				<li>請確認:</li>
				<li>1. 您的電腦是否可以偵測到P2V硬體設備。</br>
				Windows XP：請到控制台 / 硬體 / 裝置管理員 /影像裝置中查看，確認是否有出現USB視訊裝置等名稱。</li>
				<li><img src="images/trouble_/faq_p2v_01.jpg"></li>
				<li>Windows Vista／Win 7 ：請到控制台 / 系統 / 裝置管理員 / 影像裝置中查看，確認是否有IPEVO Point 2 View裝置名稱。</li>
				<li><img src="images/trouble_/faq_p2v_02.jpg"></li>
				<li>如果沒有出現USB裝置名稱(或出現驚嘆號者)，建議您先將之前的P2V程式完整移除後再重新安裝最新版本試試看。</li>
				<li>2. 因P2V在使用時僅能被一個應用程式控制，所以建議您在搭配其他應用軟體使用前，可先將P2V軟體關閉，以免影像來源被佔用而無法在電腦軟體或是視訊會議程式(MSN,Skype..)中看不到P2V的影像畫面。</br>
				3. 部分應用程式開啟時，就會進行裝置檢查，所以請先將使用的應用程式關閉、並重新將P2V連接上電腦，在重新開啟程式後就可正常見到影像。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為何P2V 在Mac OS10.6上無法運作？</li>
				<li>請至IPEVO官網下載最新版本軟體即可使用。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為何影像一直處於模糊或無法對焦的狀態？</li>
				<li>請將自動對焦設定為”C”(連續對焦)，P2V 將會持續自動對焦。若您想要手動對焦，請直接按下對焦鍵</li>
				<li><img src="images/trouble_/faq_p2v_04.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為何P2V 影像畫面動作非常緩慢？</li>
				<li>因為解析度愈高，需要之電腦運算效能也愈高，請於相機模式中，減低P2V 解析度設定。</li>
				<li><img src="images/trouble_/faq_p2v_05.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為何當我使用P2V拍攝影片的時候，其他程式速度卻被拖慢？</li>
				<li>因為解析度愈高，需要之電腦運算效能也愈高，請於相機模式中，減低P2V 解析度設定。若您目前不需使用P2V，也可將其軟體關閉，以恢復電腦系統效能。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為何拍攝中，當我移動P2V時，會導致畫面中的影像扭曲？</li>
				<li>請嘗試減慢移動速度，或於相機模式中，減低P2V 解析度設定。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為何我使用其他第三方軟體搭配P2V時(如PhotoBooth)，所拍攝的畫面與實際相反？</li>
				<li>因部分軟體，預設將畫面水平翻轉。請在軟體設定中，將鏡像或水平翻轉設定取消即可。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>如果在Windows上使用P2V，解析度設定為640x480時可正常顯示，如果將解析度調高後會畫面會停格，或有畫面延遲的現象，該如何處理？</li>
				<li>請檢查電腦中是否有安裝K-lite編碼/解碼程式，請將此軟體移除，或者請按以下連結下載ffdshow程式安裝即可。</li>
				<li><a href="http://support.ipevo.com.tw/download/cs/P2V/ffdshow.exe">ffdshow程式</a></li>
			</ol>
		</li>
	</ul>
