			<ol>
				<li>如何啟動來電轉接功能？</li>
				<li>來電轉接功能是指當您的skype離線時或者是響鈴約15秒後未接聽，您可以將來電轉接至其他號碼。您可以在（選單）（設定）（來電轉接和語音信箱）啟動並設定（轉接號碼）。（提醒您此功能會根據Skype費率扣取skyout點數）</li>
