			<h3>
				<span class="date">September 1, 2014</span>
				IPEVO 全新人聲強化網路會議機「VX-1」 有效提升企業遠距溝通效率
			</h3>
			<blockquote class="clearfix">
				<img src="images/press-vx1.jpg" alt="">
				<p>網路會議的語音品質決定了企業遠距溝通的效率與成本。IPEVO愛比科技推出全新網路會議機「VX-1」，除了大幅改善網路會議的收音與擴音，內置特殊的人聲強化音效晶片，聲音傳輸後仍保有自然渾厚的品質，讓談話者說得輕鬆，聆聽者聽得仔細，會議輕鬆自在有效率。</p>
			</blockquote>
			<p>跨區業務報告、產品開發協商或是行銷提案，用Skype、Google Hangouts、Cisco WebEX等VoIP通訊平台進行網路會議，已是企業遠距溝通的首選方案。然而效率不彰是造成會議成本居高不下的原因；尤其器材方面，電腦內建喇叭和麥克風的效能往往不夠好，與會者得費力說話和聆聽的狀況下，不僅溝通品質不佳，若是誤聽、誤判衍生出錯誤的決策，那可更加得不償失了。</p>
			<p>IPEVO愛比科技為因應廣大企業溝通需求，推出全新網路會議機「VX-1」。VX-1內建五個高感度麥克風（一個全向、四個單向麥克風），與兩個DSP數位音效處理器（Digital Sound Processor），收音與放音功能皆大幅提升。16-bit DSP內建的硬體加速與回音消除和降噪技術，使VX-1的聲音品質清晰可辨又穩定。預設的全向麥克風模式，五公尺內360度皆可收音，選擇使用單向麥克風時，更可加強區域收音的降噪處理。</p>
			<p>一般聲音經由DSP的處理傳送會失去細節，而許多高階機種在追求清晰的同時，反而將人聲壓縮得更加扁平不自然。VX-1特別多採用第二個特殊DSP來重建低頻、強化自然人聲，在不犧牲聲音清晰度的前提下，讓人聲更具厚度與廣度，與會者更能掌握對方說話的語氣與細微之處，從容聆聽或推敲話中意涵，雙方會談將更加順利。</p>
			<p>愛比科技執行副總張朝翰進一步說明：「人在直覺上決定通話時使用的音量，不是基於對方是否聽得清楚，而是基於自己是否聽得清楚。由於VX-1改善播放音質與音量，使用VX-1的一方因為對方的聲音播放出來很清晰，自然會覺得用平常的方式說話即可，不需要用力地對話，輔以 VX-1 較好的收音，對方還是能夠聽得清楚。如果會談兩方都採用 VX-1，這樣兩邊的收音與播音都會很好，大家也都會講得很輕鬆。」</p>
			<p>VX-1網路會議機不僅PC、Mac USB埠隨插即用，還特別設計了iOS使用模式，只需以內附3.5mm線連接iPhone、iPad與VX-1，不經電腦，利用Skype App照樣召開網路會議，若是行動裝置搭載了4G網路，更不受區域網路的限制。VX-1上的LED顯示燈號讓收音和靜音狀態一見即明，音量控制旋鈕單手即能操作，使用上簡便直觀。</p>
			<p>與其使用克難的設備，讓低效率的運作造成高成本的後果，萬元有找的全新人聲強化網路會議機種，值得企業採購升級。</p>
			<p style="font-weight:bold;">VX-1係台灣設計、台灣製造。更多產品資訊、規格請見：<a href="http://www.ipevo.com.tw/vx-1">www.ipevo.com.tw</a> <br>建議售價  NTD 6,490元</p>

