			<span class="date">October 21, 2014</span>
			<h3>IPEVO 獲選為美國《CIO Review》雜誌 2014年「20大最有前景的教育科技服務供應商」</h3>

			<blockquote class="clearfix">
				<img src="images/cio_review_top20.gif" alt="" width="262" height="">
				<p>
				美國《CIO Review》雜誌從上千家企業中，評選出2014年20大最有前景的教育科技服務供應商，IPEVO愛比科技名列其中。IPEVO CEO Royce洪裕鈞道出經營核心：「IPEVO產品不僅簡單易用，合理的售價讓預算不足的學校，不會被排除在實現教育目標的夢想之外。」目前已有超過35%的美國公私立中小學，採用IPEVO的產品，IPEVO正邁開穩健的腳步，讓每間教室的學生更投入學習、老師的教學更有效率。</p>
			</blockquote>

			<p>美國《CIO Review》雜誌從上千家企業中，評選出2014年前20名最有前景的教育科技服務供應商，IPEVO愛比科技名列其中。《CIO Review》表示，互動白板、數位實物攝影機正急遽改變教室裡的學習方式，老師可能從講授傳統的書本，轉變成將DNA模擬細胞分裂的3D過程投影出來，讓學生更能投入學習。而IPEVO正位於時代改變的中心，為學校提供「簡單、強大、價格可負擔」的互動工具。</p>
			<p>《CIO Review》稱IPEVO的CEO兼設計主管的Royce洪裕鈞為「創造革新的人」。「我們相信，學校和老師從使用我們的產品中獲益，比單純的交易更加寶貴。」Royce在接受專訪時如此開頭。此一思惟也反映在IPEVO產品的破壞性價格上。「許多互動教學設備非常昂貴，需要專業人員的安裝，老師還必須常常接受培訓才能將其融入日常課程。」Royce說，「這排擠了低預算的學校，我們也見到很多老師苦於教學的同時還要嫺熟複雜的科技。」這就是IPEVO創造差異的起點。</p>
			<p>IPEVO的產品種類多樣，包括實物攝影機、網路會議機以及互動式電子白板，目標是讓全美國約3百萬所教室，提升教學和學習上的互動性和效率。相較於2,000美元以上的傳統互動白板，IS-01互動白板系統僅美金149元的售價最令人驚豔；69美元的P2V實物攝影機，讓老師得以拍下和投影任何教材。Royce說：「我們產品合理的售價，讓任何人不會因為預算的限制，被排除在實現教育目標的夢想之外。」</p>
			<p>此外，針對資源特別有限的教師們，IPEVO開辦了「許願池（Wishpool）」計劃，讓老師們寫下IPEVO的產品如何能幫助他們的課程，IPEVO每月從中選出一些老師發送免費的產品，並鼓勵老師分享他們的教學故事和照片。「『許願池』不僅提供老師所需要的教室科技，我們也能夠基於真實教室直接的反饋意見，設計出更好的產品。」Royce說，「這種基於使用者經驗的產品設計流程，確保IPEVO的產品直觀易用，老師可以專注於教學，而不是忙著解釋設備如何運作。 不僅如此，我們的產品刻意只留下最必要的功能配置，用最簡單的技術，讓教師最大限度地實現他們的教學目標。」</p>
			<p>在短短的時間內，IPEVO已經成為全美K-12學校（台灣學制的小學到高中）仰賴的夥伴。為了改善互動教室體驗，西維吉尼亞州杭廷頓中學（Huntington Middle School）的資訊整合專員藍道（Randall G. Black），在2013年底時為學校導入21組P2V實物攝影機；調查顯示，僅一個月後，74%的老師已經安裝好P2V，79%在安裝軟體時完全沒有問題，而58%的教師已經將P2V應用在他們的教學上，如此卓越的成效，讓學校決定採購更多的IPEVO產品。</p>
			<p>有了許多像上述的成功案例，IPEVO的未來似乎一片光明。IPEVO亦正在開發與iPad等其他裝置協作的新產品，隨著這些新產品的釋出，更加能提升這些裝置在教學環境中的效能。如今，擁有超過35%的美國公私立中小學採用IPEVO的產品，IPEVO正邁開穩健的腳步，努力讓每間教室的學生更投入學習、老師的教學更有效率。</p>
