                <h3>FAQ常見問題</h3>
                <ul class="faq">

                    <li class="faq-q">當 iZiggi-HD 無線連接至 iPad、Mac 或 PC 時，其可接受的最大距離為何？</li>
                    <li class="faq-a">iZiggi-HD 的有效範圍是 10公尺。實際範圍可能受環境因素，如障礙物和其它無線訊號所影響。請調整相對位置，以獲得最佳效果。</li>

                    <li class="faq-q">iZiggi-HD 可以連接到多台平板裝置嗎？</li>
                    <li class="faq-a">由於頻寬和系統限制，我們建議一次只使用一台裝置來顯示 iZiggi-HD 即時取景影像。使用二台或更多裝置來顯示 iZiggi-HD 即時影像，可能會因為頻寬或系統資源不足而導致影像延遲或凍結。</li>

                    <li class="faq-q">我的教室沒有配備 Wi-Fi 網路，仍然可以無線使用 iZiggi-HD 嗎？</li>
                    <li class="faq-a">教室不需要配備 Wi-Fi 設備也可無線使用 iZiggi-HD。只需要準備 iPad 或可以接收 Wi-Fi 訊號的 PC 或 Mac。把這些裝置連接到 iZiggi-HD 的 SSID，就能觀看即時影像。</li>

                    <li class="faq-q">可以用我的桌上型電腦或 iMac 無線連接至 iZiggi-HD嗎？</li>
                    <li class="faq-a">可以的。你只需要擁有可接受 Wi-Fi 訊號的桌上型電腦或 iMac 即可無線使用 iZiggi-HD。</li>

                    <li class="faq-q">iZiggi-HD 的即時影像可以透過網頁瀏覽器觀看嗎？</li>
                    <li class="faq-a">可以。不過，不同的瀏覽器處理 iZiggi-HD 即時影像的方式有所不同，因此可能會有各種相容性問題。因此，我們還是推薦以 IPEVO Presenter 軟體或 IPEVO Whiteboard App 獲得最佳體驗。</li>

