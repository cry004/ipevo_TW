<div id="trouble_Wrap">
	<h1>常見問答</h1>
	<ul>
		<li>
			<ol>
				<li>電腦接上FR-33.2話機，Skype聲音從喇叭出來，沒從話機出來？</li>
				<li>若Skype的聲音無法從FR-33.2播放出來，那麼，先開放「FR-33.2控制台」，點選「音訊設定」標籤頁，在「Skype音訊裝置選擇」項目 中，將「聲音輸出」、「聲音輸入」、「鈴聲裝置」三各項目得下拉選單，分別改成「FR-33.2」音效裝置，然後按下「確定」鍵，即可完成設定。</li>
				<li style="text-align: center;"><img src="images/trouble_/fr332_fq_c06.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>電腦連接上FR-33.2話機，其他軟體或遊戲的音效無法正常從喇叭發音？</li>
				<li>先進入「IPEVO Control Center」、點選「音訊設定」分頁後，按下「開啟視窗系統音效控制面版」，將「音效播放預設裝置」指定在電腦原本的音效卡裝置，其他音樂或遊戲軟體即可正常發音。</li>
				<li style="text-align: center;"><img src="images/trouble_/fr332_fq_c07.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>如何將FR-33.2來電鈴聲設為靜音？</li>
				<li>開啟「IPEVO Control Center」，點選「其他設定」分頁，將「鈴聲種類」改成「靜音」，再按下「關閉」即可。</li>
				<li style="text-align: center;"><img src="images/trouble_/fr332_fq_c12.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>接上FR-33.2話機後，電腦仍找不到此裝置？</li>
				<li>當您的FR-33.2插入電腦的USB埠後，如果您的電腦無法自動偵測到時，您可以使用下面的方法來讓電腦找到FR-33.2。</li>
				<li>將FR-33.2從您電腦的USB埠上拔出</br>再重新將FR-33.2插入至另一個USB埠。FR-33.2重新插入後，您的電腦是否能正常的偵測到話機。(如果您的電腦能無法偵測到FR-33.2，那請繼續執行下個步驟) </br>點選電腦桌面上的「我的電腦」按下滑鼠右鍵，然後選擇「內容」</li>
				<li><img src="images/trouble_/fr332_fq_c13.jpg"></li>
				<li>點選「硬體」分頁後，在選擇裝置管理員</li>
				<li><img src="images/trouble_/fr332_fq_c14.jpg"></li>
				<li>點選裝置管理員選項後，選擇「執行」下的「硬體變更掃描」，來重新執行硬體掃描</li>
				<li><img src="images/trouble_/fr332_fq_c15.jpg"></li>
				<li>掃描後，您的電腦應該可以正確找到FR-33.2話機</li>
				<li><img src="images/trouble_/fr332_fq_c16.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>在Mac啟動free.2控制台時，畫面出現「應用程式free.2未預期的結束」？</li>
				<li>有可能是因為系統預設字型被移除，請到應用程式/ 字體簿確認電腦是否安裝 “BiauKai”或”Apple Ligothic”字型。</li>
				<li>在所有字體資料匣需有「BiauKai(標楷體)」或「Apple Ligothic(蘋果儷中黑)」字型，若所有字體資料匣裡沒有這二種字型請自行下載安裝。「BiauKai」或「Apple Ligothic」是Apple所提供的預設字型，有可能是誤刪或為加快電腦速度使用某些瘦身軟體而將預設字型刪除。 </br>若「BiauKai」或「Apple Ligothic」字型只是被關閉，僅需將其開啟即可。</li>
			</ol>
		</li>
	</ul>
