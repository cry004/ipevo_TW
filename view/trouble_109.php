			<ol>
				<li>為什麼電話突然沒有聲音？</li>
				<li>有可能是啟動靜音功能，請先檢查話機待機話面是否有音符禁止符號出現，請按"#"鍵約1~3秒來啟動／開啟靜音功能。</li>
			</ol>
		</li>
