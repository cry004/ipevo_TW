				<li>建議您可以參考以下方式</li>
				<li>請確認您的S0-20話機韌體版本是否已升級至最新版本，您可以在話機（選單）（設定）（關於skype）（軟體版本）中查詢，目前最新版本為1.2.0.29。</br>建議您也可以採用手動加入無線網路的方式。您可以在（選單）（設定）（網路）（範圍內的網路）選擇您的無線基地台後按（設定選項）（手動加入網路）輸入無線基地台相關資訊 </br>請檢查您的無線基地台是否有較新版本的韌體提供升級。</br> 經 測試WPA／WPA2模式較WEP穩定及安全，建議您可以將無線傳輸模式設定為WPA／WPA2模式。市面上某些無線基地台僅支援無線傳輸 802.11／N 或出廠預設設定在N模式，因S0-20僅支援802.11 b/g，也有可能造成無法正常連入的原因之一。（請參考您的無線基地台使用手冊或上網至該廠牌的官方網頁查詢相關設定） </br>如果還是無法正常連線，煩請您提供無線基地台相關資料並email至我們客戶信箱 <a href="mailto:service@ipevo.com">service@ipevo.com</a>，我們將儘快為您測試處理。(例如：廠牌／型號／無線模式(802.11b/g/n)／加密模式(WEP.WPA....)／TKIP, AES)</li>
			</ol>
		</li>
