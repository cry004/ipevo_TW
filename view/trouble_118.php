<div id="trouble_Wrap">
	<h1>常見問答</h1>
	<ul>
		<li>
			<ol>
				<li>播放音樂時按著 Tubular 的複合功能鍵，為什麼不能跳至上一首、下一首？</li>
				<li>支援AVRCP (Audio/Video Remote Control Profile) 的藍牙裝置與Tubular 搭配時，方可從 Tubular 控制所有播放功能。
				iPad/iPhone/iPod Touch 只支援部份AVRCP (Audio/Video Remote Control Profile) 功能，詳情請閱覽 <a href="http://support.apple.com/kb/HT3647?viewlocale=zh_TW" target="_blank">Apple 網站</a>。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>左右喇叭重新連接後為什麼沒有立體聲？</li>
				<li>左右喇叭分開後 Tubular 會換至左喇叭以單聲道播放，須關掉 Tubular 並重接左右喇叭再重開 Tubular 才有立體聲。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為什麼不能從我的藍牙裝置控制 Tubular 音樂播放？</li>
				<li>您的藍牙裝置須支援AVRCP (Audio/Video Remote Control Profile)。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>如何重新連結 Tubular 與已配對的藍牙裝置？</li>
				<li>按複合功能鍵即可重新連結 Tubular 與已配對的藍牙裝置。
				若是您的藍牙裝置不支援此功能，請參考快速安裝指南配對藍牙裝置。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>LED 指示燈為什麼閃爍不停？</li>
				<li>這表示電源不足，請為  Tubular 充電。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>藍牙識別碼是什麼？</li>
				<li>0000(零零零零)。</li>
			</ol>
		</li>
	</ul>
</div>