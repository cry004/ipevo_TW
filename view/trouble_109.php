				<li>如果您在設定時已選擇（儲存您的Skype帳號與密碼，以便在每次啟動時自動登入），代表話機在重新啟動、網路狀況不穩定或斷線時，待恢復連線後可自動登 入。如果您是手動登出skype帳號，代表您不希望繼續使用此skype帳號，基於帳號密碼安全性考量，您必須在下次登入Skype時重新輸入帳號及密碼。</li>
