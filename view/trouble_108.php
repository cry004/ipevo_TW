	<h1>常見問答</h1>
	<ul>
		<li>
			<ol>
				<li>X1-N6可以正常發聲，但對方聽不到我的聲音？</li>
				<li><span style="font-weight: bold">a. 調整X1-N6麥克風音量</span></br>可能是X1-N6麥克風感度過低所導致，請將麥克風感度調高。以Windows XP為例，請到開始> 設定> 控制台> 聲音及音訊裝置，在「音訊」的頁面中，將音效錄音的預設裝置設定為X1-N6，並建議將麥克風調整到中間的音量以達到最佳的通話品質。</li>
				<li><span style="font-weight: bold">b. 確認是否誤切到靜音模式</span></br>當X1-N6中央的LED紅色圓形光圈持續發亮，表示目前為靜音模式，對方將無法聽到你的聲音，請切換靜音鍵以恢復正常通話。</li>
				<li style="text-align: center;"><img src="images/trouble_/x1n6_fq_c01.jpg"></li>
			</ol>
		</li>
		<li>
			<ol>
				<li>X1-N6無法正常發聲？</li>
				<li>當通話時對方聽不到自己的聲音，很可能是誤把麥克風的音量調整成靜音狀態，這時：</li>
				<li><span style="font-weight: bold">a. 確認電源是否開啟</span></br>請確認側面開關已經打開，此時X1-N6中央的LED綠色圓形光圈會持續發亮。</li>
				<li><span style="font-weight: bold">b. 調整音量</span></br>調整X1-N6的音量調整軸，9表示最大聲，0表示最小聲。並確認X1-N6的喇叭是否已調整到建議的音量。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>電腦已接上X1-N6，但通訊軟體的聲音仍然從電腦的喇叭發聲，沒從X1-N6出來？</li>
				<li>請將通訊軟體的音效設定指定到X1-N6。以Skype為例，請確認「功能－設定選項－音效設定」的聲音輸出及輸入已切換到X1-N6。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>電腦接上X1-N6後，其它軟體或遊戲的音效無法正常從電腦喇叭發聲？</li>
				<li>請到開始 > 設定 > 控制台 > 聲音及音訊裝置，在「音訊」的頁面裡，將音效撥放及音效錄音的預設裝置皆設定為您電腦原本的音效卡裝置。</li>
				<li style="text-align: center;"><img src="images/trouble_/x1n6_fq_c02.jpg"></li>
			</ol>
		</li>
	</ul>
