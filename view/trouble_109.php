				<li>話機的通話和待機時間不是很穩定，是電池的問題嗎？</li>
				<li>Wi-Fi無線話機的通話及待機時間取決於無線網路環境、訊號強度、話機使用頻率，請儘量保持良好的無線環境，倘若你的電池電力一直出現不尋常的問題，請Email至我們客服信箱<a href="mailto:service@ipevo.com">service@ipevo.com</a>，我們將有專人為您服務。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>第一次登入Skype帳號需花上很久的時間？</li>
				<li>第一次登入Skype帳號時，需要花上比較久的時間將聯絡人名單下載至你的無線話機，請耐心等待。若一直無法成功登入，請確認無線網路環境及訊號是否穩定。</li>
			</ol>
		</li>
		<li>
			<ol>
				<li>為什麼通話時有時候聲音很大，有時卻很小聲，偶爾還會有斷斷續續的？</li>
				<li>有可能為對外頻寬不足或連線狀況不穩定所造成。請檢查您的對外網路連線品質是否穩定，或者是在通話時是否有其他電腦（例如續傳軟體）佔用對外頻寬。建議您可以先將此類軟體暫時關閉後再通話試試看，或者也建議您將數據機，無線分享器關機後３分鐘後再打開後重新撥打試試看。</li>
			</ol>
		</li>
